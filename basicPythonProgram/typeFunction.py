# type() function returns the type of a variable
# In python datatypes are considered as classes and the variables are the objects of that classes

x=10;
y=10.12;
z=True;
w=input("Enter any number:");

print(f"The type of x is {type(x)}");
print(f"The type of y is {type(y)}");
print(f"The type of z is {type(z)}");
print(f"The type of w is {type(w)}");